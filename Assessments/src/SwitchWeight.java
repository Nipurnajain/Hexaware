
public class SwitchWeight {

	public static void main(String[] args) {
		int Weight;
		switch (Weight) {
		  case (Weight<=50):
		   System.out.println("Light");
		   break;
		  case (Weight>50 && Weight <=100):
		   System.out.println("Medium");
		   break;
		  case (Weight>100):
		   System.out.println("Heavy");
		   break;
		   }

	}

}
